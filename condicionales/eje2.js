let num = parseInt(prompt(" ingrese un numero"));

if(isNaN(num)) {
    console.log("Por favor, ingrese un número válido.");
} else if (num > 0 ) {
    console.log(`Este numero es positivo`);
} else if (num < 0) {
    console.log(`Este numero es negativo`);
} else {
    console.log(`ESte numero es pósitivo`);
    
}

