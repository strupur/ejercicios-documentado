// El método toString() en JavaScript se utiliza para convertir diferentes tipos de datos a su representación de cadena (string). Es una función muy versátil y se puede aplicar a diferentes tipos de datos como números, arrays, objetos, entre otros.

let numero = 123;
let cadenaNumero = numero.toString();
console.log(cadenaNumero); // "123"
console.log(typeof cadenaNumero); // "string"

let arreglo = [1, 2, 3, 4];
let cadenaArray = arreglo.toString();
console.log(cadenaArray); // "1,2,3,4"
console.log(typeof cadenaArray); // "string"